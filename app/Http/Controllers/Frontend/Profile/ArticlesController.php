    {
        $advices = Advice::where('user_id', Auth::id())
            ->orderBy('created_at', 'desc')
            ->paginate();

        $recipes = Recipe::where('user_id', Auth::id())
            ->with(['categories'])
            ->orderBy('created_at', 'desc')
            ->paginate();
            // dd($recipes);
        return view('frontend.profile.articles.index', [
            'advices' => $advices,
            'recipes' => $recipes
